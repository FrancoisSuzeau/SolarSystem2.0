#include "InputDevices.hpp"
using namespace Engine::InputDevices;


/***********************************************************************************************************************************************************************/
/*********************************************************************** Constructor and Destructor ********************************************************************/
/***********************************************************************************************************************************************************************/
KeyInput::KeyInput()
{
    //initialize key array
    for (int i(0); i < SDL_NUM_SCANCODES; i++)
    {
        m_keys[i] = false;
    }
    
}

KeyInput::~KeyInput()
{
    
}

/***********************************************************************************************************************************************************************/
/************************************************************************************ UpdateEvents *********************************************************************/
/***********************************************************************************************************************************************************************/
void KeyInput::updateEvents(SDL_Event event)
{
    
    switch (event.type)
    {
        //activate a key
    case SDL_KEYDOWN:
        m_keys[event.key.keysym.scancode] = true;
        break;
    case SDL_KEYUP:
        m_keys[event.key.keysym.scancode] = false;
        break;
    default:
        break;
    }

}

///***********************************************************************************************************************************************************************/
///***************************************************************************************** getKey **********************************************************************/
///***********************************************************************************************************************************************************************/
bool KeyInput::getKey(const SDL_Scancode key) const
{
    return m_keys[key];
}